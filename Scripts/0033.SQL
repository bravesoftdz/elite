CREATE VIEW REL_ESTOQUE(
    BARRA,
    DESCRICAO,
    PRECOVENDA,
    CODEMPRESA,
    QUANTIDADE)
AS
SELECT
  PRO.BARRA,
  PRO.DESCRICAO,
  PRO.PRECOVENDA,
  IND.CODEMPRESA,
  SUM(IND.QUANTIDADE)
FROM
  PRODUTOS PRO
  JOIN INDIVIDUAIS IND ON
    (PRO.CODIGO = IND.CODPRODUTO)
WHERE
  PRO.PS = 'P' AND
  IND.VENDIDO = 'N'
GROUP BY
  PRO.BARRA,
  PRO.DESCRICAO,
  PRO.PRECOVENDA,
  IND.CODEMPRESA
!

grant select ON REL_ESTOQUE TO PUBLIC!
