CREATE PROCEDURE TROCA_SENHAWEB (
    CODUSUARIO INTEGER,
    SENHAATUAL VARCHAR(10),
    SENHANOVA VARCHAR(10),
    SENHACONFIRMACAO VARCHAR(10))
RETURNS (
    RESULTADO INTEGER)
AS
DECLARE VARIABLE TEMP_ATUAL VARCHAR(10);
begin

  /* RESULTADO EH ERRADO POR DEFAULT */
  RESULTADO = 1;

  /* LEIO A SENHA ATUAL */
  SELECT
    SENHAWEB
  FROM
    CLIENTES
  WHERE
    CODIGO = :CODUSUARIO
  INTO
    :TEMP_ATUAL;

  IF (TEMP_ATUAL IS NULL) THEN
    TEMP_ATUAL = '';

  /* SENHA ATUAL BATE? */
  IF (TEMP_ATUAL = SENHAATUAL) THEN
   BEGIN

     /* AS SENHAS NOVAS CONFEREM? */
     IF (SENHANOVA = SENHACONFIRMACAO) THEN
      BEGIN

        /* ATUALIZO A SENHA */
        UPDATE
          CLIENTES
        SET
          SENHAWEB = :SENHANOVA
        WHERE
          CODIGO = :CODUSUARIO;

        RESULTADO = 0;

        WHEN ANY DO RESULTADO = 1;

      END ELSE RESULTADO = 1;

   END ELSE RESULTADO = 1;

end

^

SET TERM ; ^

